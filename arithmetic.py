print('2 + 7 = ' + str(2+7))    # Addition
print('2 - 7 = ' + str(2-7))     # Subtraction
print('4 * 6 = ' + str(4*6))     # Multiplication
print('2 ** 3 = ' + str(2**3))      # Exponent
print('13 / 5 = ' + str(13/5))      # Division
print('9 % 4 = ' + str(9 % 4))    # Modulus, remainder of integer division
print('13 // 5 = ' + str(13 // 5))     # Floor division
print('-13 // 5 = ' + str(-13 // 5))    # Floor division
